package edu.smavs.basic01;

public class DemoChar {
    public static void main(String[] args) {

        char s = 'T';
        System.out.println(s);

        String a = "Amit";  // String is a final class you can not change value of variable
        System.out.println(a);

        s = 'M';
        System.out.println(s);
    }
}
